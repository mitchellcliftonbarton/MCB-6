			<p>Mitchell Barton is an artist working in Salt Lake City, UT, USA.</p>

			<p>Recent(ish) projects include:</p>

			<ul>
				<li>“Razorbacks” — an exhibition at the Utah Museum of Contemporary Art.</li>
				<li>
					“Three Basketball Games” — a book with photographs and text regarding three basketball
					games he watched from his living room. The photographs were made through a process of
					cutting, photoshopping, gluing, drawing, printing, and scanning.
				</li>
				<li>“Artspeak Ipsum” — a lorem ipsum generator based on International Art English.</li>
			</ul>

			<p>
				He also runs a little gallery space in his basement furnace room called <a
					href="https://washer-dryer-projects.com">Washer / Dryer Projects</a
				> that is hardly ever open to the public but always open on the internet.
			</p>

			<p>
				Apart from making art, he makes custom websites for people at <a
					href="https://cold-rice.info">Cold Rice LLC</a
				>.
			</p>

			<p>
				His work has been shown online and in real life at Tiger Strikes Asteroid, Arcade on
				Stadium, Tropical Contemporary, UMOCA, CUAC, Der Grief, A New Nothing, Self Publish Be
				Happy, The Daily Lazy, In The In-Between, and others.
			</p>

